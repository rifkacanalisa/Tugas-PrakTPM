package com.example.intentandrecycleview;

public class GroupModel {
    public String groupName, groupDebut, groupEnt;
    public int groupImage;

    public String getGroupName() {
        return groupName;
    }

    public void setGroupName(String groupName) {
        this.groupName = groupName;
    }

    public String getGroupDebut() {
        return groupDebut;
    }

    public void setGroupDebut(String groupDebut) {
        this.groupDebut = groupDebut;
    }

    public String getGroupEnt() {
        return groupEnt;
    }

    public void setGroupEnt(String groupEnt) {
        this.groupEnt = groupEnt;
    }

    public int getGroupImage() {
        return groupImage;
    }

    public void setGroupImage(int groupImage) {
        this.groupImage = groupImage;
    }
}
